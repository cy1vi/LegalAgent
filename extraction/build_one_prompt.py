def build_prompt_no_template(accusation_name):
    """
    构建无需模板的通用 Prompt：让 LLM 基于罪名自主提取三类因素
    """
    prompt = f"""你是一名资深刑事法官，请根据以下案件事实，针对“{accusation_name}”这一罪名，从原文中提取以下三类法律要素的**具体关键词或短语**：

1. 【定罪因素】：构成该罪名所必需的客观行为、主观目的、结果要件等（如“非法占有目的”“持刀伤人呢”等）。
2. 【加重因素】：导致刑罚加重的情节（如“数额巨大”“多次作案”“造成严重后果”等）。
3. 【减轻因素】：导致刑罚减轻或从宽处罚的情节（如“自首”“退赃退赔”“认罪认罚”“从犯”等）。

提取规则：
- 必须严格引用**案件事实原文中的词句**，不得改写、概括或推理。
- 如果某类要素未在原文中出现，请返回空列表 []。

输出要求：
1. 严格按照以下JSON格式输出结果：
    {{
        "core_factors": ["定罪因素"],
        "aggravating_factors": ["找到的从重处罚情节关键词"],
        "mitigating_factors": ["找到的从轻处罚情节关键词"],
    }}
案件事实：
"""
    return prompt